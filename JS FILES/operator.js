//2.Logical Operator

// AND 

// True  && True  --------->  True
// True  && False --------->  False
// False && True  --------->  False
// False && False --------->  False

console.log(10 >9  && 7 ==7)             //o/p----> true

console.log(1 == '1' && 7 === '7')       //o/p----> false

console.log(6 >= 7 &&  8 == '8')        //o/p----> false
 
console.log(7 !== '7' &&  10 > 10)       //o/p----> false




console.log('*****************************************************')



// OR 
// True  || True  --------->  True
// True  || False --------->  True
// False || True  --------->  True
// False || False ---------> False


console.log(12 > 10 || 10===10)         //o/p----> true

console.log('15' === '15' || 25 != 25)  //o/p----> true

console.log('20' !== '20' || 20 > 10)   //o/p----> true

console.log(10==='10' || 20 < 18)       //o/p----> false
 

console.log('****************************************************')

// NOT
// True --- False 
// False --- True
console.log(!7 === 7)               //o/p---> false

console.log(!(8 !== 8))             //o/p---> true


