//!= , == ,===, !==
 
// === ----------> value and type
// !== ---------->  value and type
// ==  ----------> value 
// !=  ----------> value

console.log(12 == '12')         //o/p----> true
console.log(12 === '12')        //o/p----> false(value is same but type is diffrent number !='string')
console.log(12 === 11)          //o/p----> false
console.log(12 === 12)          //o/p----> true
console.log(12 != 11)           //o/p----> true 
console.log(12 !== '12')        //o/p----> true
console.log(12 != '12')         //o/p----> false



